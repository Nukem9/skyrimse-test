std::map<uint64_t, uintptr_t> Mapping;

uintptr_t ResolveOffset(uint32_t RelOffset, uint32_t Version)
{
	uint64_t key = ((uint64_t)Version << 32) | RelOffset;
	auto itr = Mapping.find(key);

	if (itr != Mapping.end())
		return itr->second;

	AssertMsg(false, "Trying to resolve offset with no available mapping");
	return 0;
}

